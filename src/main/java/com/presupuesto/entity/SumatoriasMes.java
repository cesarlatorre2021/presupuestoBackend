package com.presupuesto.entity;

public class SumatoriasMes {
	
	private long sumaIngresos;
	private long sumaGastos;
	private long diferencia;
	
	public long getSumaIngresos() {
		return sumaIngresos;
	}
	
	public void setSumaIngresos(long sumaIngresos) {
		this.sumaIngresos = sumaIngresos;
	}
	
	public long getSumaGastos() {
		return sumaGastos;
	}
	
	public void setSumaGastos(long sumaGastos) {
		this.sumaGastos = sumaGastos;
	}
	
	public long getDiferencia() {
		return diferencia;
	}
	
	public void setDiferencia(long diferencia) {	
		this.diferencia = diferencia;
	}

}
